					<p>В соответствии с Федеральным законом от 24.07.2007 г. № 209-ФЗ «О развитии малого и среднего предпринимательства в Российской Федерации», Законом Ямало-Ненецкого автономного округа от 25.12.2008 г. № 128-ЗАО «О развитии малого и среднего предпринимательства в Ямало-Ненецком автономном округе», Приказом Министерства экономического развития Российской Федерации от 30.01.2009 г. № 31 "О мерах по реализации в 2009 году мероприятий по государственной поддержке малого и среднего предпринимательства", на основании Распоряжения Администрации ЯНАО от 02.12.2009 г. № 497-РА «О создании некоммерческой организации «Гарантийный фонд поддержки малого предпринимательства Ямало-Ненецкого автономного округа» в 2009 году создана некоммерческая организация «Гарантийный фонд поддержки малого предпринимательства Ямало-Ненецкого автономного округа». Учредителем Фонда является Ямало-Ненецкий автономный округ в лице Департамента экономики Ямало-Ненецкого автономного округа.</p>
